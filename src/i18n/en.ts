// Create a flexible type that allows different string values per language
type DeepReplace<T> = {
    [K in keyof T]: T[K] extends string
    ? string
    : T[K] extends readonly string[]
    ? string[]
    : T[K] extends object
    ? DeepReplace<T[K]>
    : never;
};

